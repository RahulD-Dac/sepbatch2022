

import java.util.Scanner;

class DiffLoop{

public static void main(String[] args)
{
		 
		 
		{
			System.out.println("For loop");
			for(int j=20; j>=1; j--)
			{
				
			System.out.println(j);				
			}
			
			System.out.println("For WHILE LOOP");
			int i= 20;
			while (i>=1)
			{
				System.out.println(i);
				i--;
 			}
			
			System.out.println("do while");
			
			do{
				System.out.println(i);
				i--;

				
			}while(i>1);
			
		}
}
}